//! Linux DNS configuration — resolver-regime detection and takeover.
//!
//! Implements `docs/os-integration.md` §3 (the resolver-regime ladder) for
//! Linux.  See that section for the full design rationale; this module adds
//! only implementation decisions.
//!
//! ## Regime ladder (detection order)
//!
//! 1. **systemd-resolved** — `/etc/resolv.conf` is a symlink whose target
//!    contains `systemd/resolve`, **or** the file contains `nameserver 127.0.0.53`.
//! 2. **NetworkManager (without resolved)** — `resolv.conf` header contains
//!    the NM signature comment, or `/run/NetworkManager` exists.
//! 3. **resolvconf/openresolv** — `resolv.conf` header contains
//!    `Generated by resolvconf`.
//! 4. **Plain resolv.conf** — none of the above.
//!
//! Detection is a **pure function** over inputs (symlink target string, file
//! contents, marker-file existence booleans), making it unit-testable on any
//! host including this macOS dev machine.
//!
//! ## Snapshot schema
//!
//! The Linux regime is stored in an **additive optional field** on
//! [`DnsSnapshot`]: `linux_regime: Option<LinuxRegime>`.  Old macOS snapshots
//! (field absent) deserialise cleanly via `#[serde(default)]` and are
//! unaffected.  The regime encodes everything `restore()` needs after a crash
//! or reboot.
//!
//! ## resolvconf deviation
//!
//! The spec (os-integration.md §3) says: "register as a resolvconf provider
//! with top priority."  This implementation instead performs an atomic-replace
//! of `/etc/resolv.conf` (keeping the original at
//! `resolv.conf.hushwarren-prev`), identical to the plain-resolv.conf path.
//! Rationale: resolvconf provider registration requires parsing
//! `/etc/resolvconf.conf`, writing to `/etc/resolvconf/resolv.conf.d/`,
//! calling `resolvconf -u`, and handling `openresolv` vs `resolvconf-ng` API
//! differences — substantial complexity with low marginal value for a P1
//! scope.  The deviation is safe because hushwarren's ownership of
//! `/etc/resolv.conf` is explicit and reversible.  Flagged here and in the
//! run summary.
//!
//! ## Root requirement
//!
//! `point_at_self()` and `restore()` return [`PlatformError::NeedsRoot`] if
//! `euid != 0`, checked before any side effect (mirrors `macos.rs`).

