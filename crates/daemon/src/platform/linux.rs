// ── Inputs for pure regime detection ─────────────────────────────────────────

/// Everything `detect_regime` needs, as plain data so it can be called from
/// tests on any host without touching real `/etc/resolv.conf`.
#[derive(Debug, Default)]
pub struct RegimeInputs {
    /// The symlink target of `/etc/resolv.conf`, if it is a symlink.
    ///
    /// `None` means the file is not a symlink (or readlink failed).
    pub symlink_target: Option<String>,

    /// The first ~1 KiB of `/etc/resolv.conf` as UTF-8.
    ///
    /// Used to detect stub IP (127.0.0.53) and header comments.
    pub resolv_conf_head: String,

    /// Whether `/run/NetworkManager` exists.
    pub run_nm_exists: bool,
}

// ── Pure regime detection (unit-testable on any host) ────────────────────────

/// Detect the active resolver regime from pre-read inputs.
///
/// This is a **pure function** — no I/O.  The caller supplies the inputs via
/// [`RegimeInputs`] so the function is unit-testable on macOS without touching
/// `/etc`.
///
/// Detection order follows `docs/os-integration.md` §3:
/// 1. systemd-resolved (symlink target contains `systemd/resolve` OR content
///    contains `nameserver 127.0.0.53`)
/// 2. NetworkManager (header comment contains `NetworkManager` OR
///    `/run/NetworkManager` exists)
/// 3. resolvconf/openresolv (header contains `Generated by resolvconf`)
/// 4. Plain resolv.conf
pub fn detect_regime(inputs: &RegimeInputs) -> LinuxRegime {
    // 1. systemd-resolved
    if let Some(target) = &inputs.symlink_target {
        if target.contains("systemd/resolve") || target.contains("systemd-resolve") {
            return LinuxRegime::SystemdResolved;
        }
    }
    if inputs
        .resolv_conf_head
        .lines()
        .any(|l| l.trim().starts_with("nameserver") && l.contains("127.0.0.53"))
    {
        return LinuxRegime::SystemdResolved;
    }

    // 2. NetworkManager (without resolved)
    if inputs.resolv_conf_head.contains("NetworkManager")
        || inputs
            .resolv_conf_head
            .contains("Generated by NetworkManager")
        || inputs.run_nm_exists
    {
        return LinuxRegime::NetworkManager;
    }

    // 3. resolvconf/openresolv
    if inputs.resolv_conf_head.contains("Generated by resolvconf") {
        return LinuxRegime::Resolvconf;
    }

    // 4. Plain
    LinuxRegime::Plain
}

// ── File-content generators (pure, unit-testable) ─────────────────────────────

/// Generate the content for the systemd-resolved drop-in file.
///
/// Written to `/etc/systemd/resolved.conf.d/hushwarren.conf`.
/// `[Resolve]` section with `DNS=127.0.0.1 ::1` and `Domains=~.` (catch-all).
pub fn resolved_dropin_content() -> String {
    // The Domains=~. directive makes resolved send all queries to our sinkhole
    // rather than only the system search domain.
    "[Resolve]\nDNS=127.0.0.1 ::1\nDomains=~.\n".to_owned()
}

/// Generate the NetworkManager conf.d drop-in that disables NM DNS management.
///
/// Written to `/etc/NetworkManager/conf.d/hushwarren.conf`.
pub fn nm_dropin_content() -> String {
    "[main]\ndns=none\n".to_owned()
}

/// Generate the `/etc/resolv.conf` content pointing at the sinkhole.
///
/// Used by the NM and plain/resolvconf takeover paths.
pub fn sinkhole_resolv_conf_content() -> String {
    // Generated-by comment allows later detection if needed.
    "# Generated by hushwarren — do not edit manually.\n\
     # Restore with: hushd restore\n\
     nameserver 127.0.0.1\n\
     nameserver ::1\n"
        .to_owned()
}

// ── Snapshot helpers (pure) ────────────────────────────────────────────────────

/// The synthetic service name used to represent the Linux resolv.conf
/// configuration in a [`DnsSnapshot`].
///
/// Encoding regime info into the service name (e.g. `"linux:resolved"`) was
/// considered but rejected: it embeds structured data in an opaque string and
/// makes snapshot JSON harder to read.  Instead, an additive
/// `linux_regime: Option<LinuxRegime>` field on the snapshot carries this.
pub const LINUX_SERVICE_NAME: &str = "linux:resolv.conf";

/// Build the [`ServiceDns`] entry for a Linux snapshot.
///
/// The `setting` records the pre-takeover state:
/// - For the plain/NM/resolvconf path, the original nameservers found in
///   `/etc/resolv.conf` (or `Dhcp` if none are found).
/// - For the systemd-resolved path, `Dhcp` (resolved provides the stub; the
///   real server list is in resolved's config, not resolv.conf).
pub fn build_linux_service(setting: DnsSetting) -> ServiceDns {
    ServiceDns {
        service: LINUX_SERVICE_NAME.to_owned(),
        setting,
    }
}

/// Parse nameserver lines from `/etc/resolv.conf` content.
///
/// Returns `Dhcp` if no parseable `nameserver` lines are found (e.g. the file
/// only has comments, or resolved uses the stub and we want to abstract away
/// the 127.0.0.53 stub IP).
pub fn parse_resolv_conf_servers(content: &str) -> DnsSetting {
    let servers: Vec<IpAddr> = content
        .lines()
        .filter_map(|line| {
            let trimmed = line.trim();
            if !trimmed.starts_with("nameserver") {
                return None;
            }
            let rest = trimmed["nameserver".len()..].trim();
            match rest.parse::<IpAddr>() {
                Ok(ip) => Some(ip),
                Err(_) => {
                    debug!(line = trimmed, "skipping unparseable nameserver line");
                    None
                }
            }
        })
        .collect();

    if servers.is_empty() {
        DnsSetting::Dhcp
    } else {
        DnsSetting::Static { servers }
    }
}

