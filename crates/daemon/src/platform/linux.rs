// ── Root check ─────────────────────────────────────────────────────────────────

/// Return [`PlatformError::NeedsRoot`] if the effective UID is not 0.
#[cfg(target_os = "linux")]
///
/// Uses `/usr/bin/id -u`; on failure, optimistically proceeds (the subsequent
/// file write will surface the real EPERM).
fn check_root() -> Result<(), PlatformError> {
    match Command::new("/usr/bin/id").arg("-u").output() {
        Ok(out) if out.status.success() => {
            let uid_str = String::from_utf8_lossy(&out.stdout);
            // PANIC-OK: parse failure defaults to uid=1, preserving safety.
            let uid: u32 = uid_str.trim().parse().unwrap_or(1);
            if uid == 0 {
                Ok(())
            } else {
                Err(PlatformError::NeedsRoot)
            }
        }
        // Cannot determine UID — optimistically proceed; the OS will reject
        // the write with EPERM and surface a CommandFailed error.
        _ => Ok(()),
    }
}

// ── Regime executor (side-effecting) ──────────────────────────────────────────

/// Perform the regime-specific takeover, given the detected `regime` and the
/// resolved `/etc/resolv.conf` path (usually `/etc/resolv.conf`).
///
/// All side effects (file writes, `systemctl`, `nmcli`) live here.
#[cfg(target_os = "linux")]
fn exec_takeover(regime: &LinuxRegime, resolv_conf: &Path) -> Result<(), PlatformError> {
    match regime {
        LinuxRegime::SystemdResolved => {
            info!("linux takeover: systemd-resolved path");
            write_resolved_dropin()?;
            systemctl_restart_resolved()?;
        }
        LinuxRegime::NetworkManager => {
            info!("linux takeover: NetworkManager path");
            write_nm_dropin()?;
            atomic_write_resolv_conf(resolv_conf, &sinkhole_resolv_conf_content())?;
            nm_reload()?;
        }
        LinuxRegime::Resolvconf => {
            // Deviation: atomic-replace instead of resolvconf provider
            // registration.  See module-level doc.
            warn!(
                "linux takeover: resolvconf detected — using atomic-replace \
                 (deviation from spec; resolvconf provider registration not implemented)"
            );
            atomic_write_resolv_conf_with_prev(resolv_conf)?;
        }
        LinuxRegime::Plain => {
            info!("linux takeover: plain resolv.conf path");
            atomic_write_resolv_conf_with_prev(resolv_conf)?;
        }
    }
    Ok(())
}

/// Undo the regime-specific takeover.
///
/// The `snapshot` carries the regime via `linux_regime` so this works after
/// a crash and reboot.
#[cfg(target_os = "linux")]
fn exec_restore(regime: &LinuxRegime, resolv_conf: &Path) -> Result<(), PlatformError> {
    match regime {
        LinuxRegime::SystemdResolved => {
            info!("linux restore: removing resolved drop-in + restart");
            remove_resolved_dropin()?;
            systemctl_restart_resolved()?;
        }
        LinuxRegime::NetworkManager => {
            info!("linux restore: removing NM drop-in + reload");
            remove_nm_dropin()?;
            restore_resolv_conf_prev(resolv_conf)?;
            nm_reload()?;
        }
        LinuxRegime::Resolvconf | LinuxRegime::Plain => {
            info!("linux restore: moving resolv.conf.hushwarren-prev back");
            restore_resolv_conf_prev(resolv_conf)?;
        }
    }
    Ok(())
}

// ── File-write helpers ────────────────────────────────────────────────────────

/// Path to the systemd-resolved drop-in directory.
#[cfg(target_os = "linux")]
const RESOLVED_DROPIN_DIR: &str = "/etc/systemd/resolved.conf.d";
/// Filename for the drop-in inside the directory.
#[cfg(target_os = "linux")]
const RESOLVED_DROPIN_NAME: &str = "hushwarren.conf";

/// Path to the NetworkManager conf.d directory.
#[cfg(target_os = "linux")]
const NM_DROPIN_DIR: &str = "/etc/NetworkManager/conf.d";
/// Filename for the NM drop-in.
#[cfg(target_os = "linux")]
const NM_DROPIN_NAME: &str = "hushwarren.conf";

#[cfg(target_os = "linux")]
fn resolved_dropin_path() -> PathBuf {
    Path::new(RESOLVED_DROPIN_DIR).join(RESOLVED_DROPIN_NAME)
}

#[cfg(target_os = "linux")]
fn nm_dropin_path() -> PathBuf {
    Path::new(NM_DROPIN_DIR).join(NM_DROPIN_NAME)
}

/// Write the systemd-resolved drop-in, creating the directory if needed.
#[cfg(target_os = "linux")]
fn write_resolved_dropin() -> Result<(), PlatformError> {
    let dir = Path::new(RESOLVED_DROPIN_DIR);
    std::fs::create_dir_all(dir)
        .map_err(|e| PlatformError::CommandFailed(format!("create {RESOLVED_DROPIN_DIR}: {e}")))?;
    let path = resolved_dropin_path();
    write_file_atomic(&path, &resolved_dropin_content())?;
    debug!(?path, "wrote resolved drop-in");
    Ok(())
}

/// Remove the systemd-resolved drop-in.  Not-found is silently ignored.
#[cfg(target_os = "linux")]
fn remove_resolved_dropin() -> Result<(), PlatformError> {
    let path = resolved_dropin_path();
    match std::fs::remove_file(&path) {
        Ok(()) => {
            debug!(?path, "removed resolved drop-in");
            Ok(())
        }
        Err(e) if e.kind() == std::io::ErrorKind::NotFound => Ok(()),
        Err(e) => Err(PlatformError::CommandFailed(format!(
            "remove {}: {e}",
            path.display()
        ))),
    }
}

/// Write the NetworkManager drop-in, creating the directory if needed.
#[cfg(target_os = "linux")]
fn write_nm_dropin() -> Result<(), PlatformError> {
    let dir = Path::new(NM_DROPIN_DIR);
    std::fs::create_dir_all(dir)
        .map_err(|e| PlatformError::CommandFailed(format!("create {NM_DROPIN_DIR}: {e}")))?;
    let path = nm_dropin_path();
    write_file_atomic(&path, &nm_dropin_content())?;
    debug!(?path, "wrote NetworkManager drop-in");
    Ok(())
}

/// Remove the NetworkManager drop-in.  Not-found is silently ignored.
#[cfg(target_os = "linux")]
fn remove_nm_dropin() -> Result<(), PlatformError> {
    let path = nm_dropin_path();
    match std::fs::remove_file(&path) {
        Ok(()) => {
            debug!(?path, "removed NM drop-in");
            Ok(())
        }
        Err(e) if e.kind() == std::io::ErrorKind::NotFound => Ok(()),
        Err(e) => Err(PlatformError::CommandFailed(format!(
            "remove {}: {e}",
            path.display()
        ))),
    }
}

/// Write content to `path` atomically (tmp file + rename in same directory).
#[cfg(target_os = "linux")]
fn write_file_atomic(path: &Path, content: &str) -> Result<(), PlatformError> {
    use std::io::Write;

    // Build a sibling tmp path in the same directory.
    let parent = path
        .parent()
        .ok_or_else(|| PlatformError::CommandFailed(format!("no parent for {}", path.display())))?;
    let file_name = path.file_name().ok_or_else(|| {
        PlatformError::CommandFailed(format!("no filename for {}", path.display()))
    })?;
    let tmp_name = format!(".{}.hushwarren.tmp", file_name.to_string_lossy());
    let tmp_path = parent.join(&tmp_name);

    let mut f = std::fs::File::create(&tmp_path)
        .map_err(|e| PlatformError::CommandFailed(format!("create {}: {e}", tmp_path.display())))?;
    f.write_all(content.as_bytes())
        .map_err(|e| PlatformError::CommandFailed(format!("write {}: {e}", tmp_path.display())))?;
    f.sync_all()
        .map_err(|e| PlatformError::CommandFailed(format!("fsync {}: {e}", tmp_path.display())))?;
    drop(f);

    std::fs::rename(&tmp_path, path).map_err(|e| {
        PlatformError::CommandFailed(format!(
            "rename {} → {}: {e}",
            tmp_path.display(),
            path.display()
        ))
    })?;
    Ok(())
}

/// Atomically overwrite `resolv_conf` with sinkhole content, saving the
/// original to `<resolv_conf>.hushwarren-prev`.
///
/// The prev-file is written before the takeover file, so if we crash between
/// the two writes, the original file is still present.
#[cfg(target_os = "linux")]
fn atomic_write_resolv_conf_with_prev(resolv_conf: &Path) -> Result<(), PlatformError> {
    let prev_path = prev_resolv_conf_path(resolv_conf);

    // Copy original to prev (best-effort — if it already exists from a
    // previous run, leave it alone so we preserve the true baseline).
    if !prev_path.exists() {
        if let Ok(original) = std::fs::read_to_string(resolv_conf) {
            write_file_atomic(&prev_path, &original)?;
            debug!(?prev_path, "saved resolv.conf baseline to prev");
        }
    }

    atomic_write_resolv_conf(resolv_conf, &sinkhole_resolv_conf_content())
}

/// Atomically write `content` to `resolv_conf` (no prev-save).
#[cfg(target_os = "linux")]
fn atomic_write_resolv_conf(resolv_conf: &Path, content: &str) -> Result<(), PlatformError> {
    write_file_atomic(resolv_conf, content)?;
    debug!(?resolv_conf, "wrote sinkhole resolv.conf");
    Ok(())
}

/// Restore `/etc/resolv.conf` from the `.hushwarren-prev` backup.
#[cfg(target_os = "linux")]
fn restore_resolv_conf_prev(resolv_conf: &Path) -> Result<(), PlatformError> {
    let prev_path = prev_resolv_conf_path(resolv_conf);
    match std::fs::read_to_string(&prev_path) {
        Ok(content) => {
            write_file_atomic(resolv_conf, &content)?;
            // Remove the prev file after a successful restore.
            if let Err(e) = std::fs::remove_file(&prev_path) {
                warn!(error = %e, ?prev_path, "could not remove prev file after restore");
            }
            debug!(?resolv_conf, "restored resolv.conf from prev");
            Ok(())
        }
        Err(e) if e.kind() == std::io::ErrorKind::NotFound => {
            // No prev file — either we never took over, or it was already
            // cleaned up.  Not an error; resolv.conf is presumably already
            // in a good state.
            debug!(?prev_path, "no prev file found during restore — skipping");
            Ok(())
        }
        Err(e) => Err(PlatformError::CommandFailed(format!(
            "read {}: {e}",
            prev_path.display()
        ))),
    }
}

/// Derive the path of the backup resolv.conf (`.hushwarren-prev` suffix).
///
/// Kept unconditionally compiled so the unit test on macOS can call it.
#[cfg(any(target_os = "linux", test))]
fn prev_resolv_conf_path(resolv_conf: &Path) -> PathBuf {
    let name = resolv_conf
        .file_name()
        .map(|n| format!("{}.hushwarren-prev", n.to_string_lossy()))
        .unwrap_or_else(|| "resolv.conf.hushwarren-prev".to_owned());
    resolv_conf.parent().unwrap_or(Path::new("/etc")).join(name)
}

// ── systemctl / nmcli ─────────────────────────────────────────────────────────

/// Run `systemctl restart systemd-resolved`.
#[cfg(target_os = "linux")]
fn systemctl_restart_resolved() -> Result<(), PlatformError> {
    run_command("systemctl", &["restart", "systemd-resolved"])
}

/// Run `nmcli general reload` (or `systemctl reload NetworkManager`).
///
/// Tries `nmcli` first; falls back to `systemctl reload NetworkManager`.
#[cfg(target_os = "linux")]
fn nm_reload() -> Result<(), PlatformError> {
    if run_command("nmcli", &["general", "reload"]).is_ok() {
        return Ok(());
    }
    // Fallback: systemctl reload.
    run_command("systemctl", &["reload", "NetworkManager"])
}

/// Run an external command, returning [`PlatformError::CommandFailed`] on
/// non-zero exit or spawn failure.
#[cfg(target_os = "linux")]
fn run_command(cmd: &str, args: &[&str]) -> Result<(), PlatformError> {
    let output = Command::new(cmd)
        .args(args)
        .output()
        .map_err(|e| PlatformError::CommandFailed(format!("failed to spawn {cmd}: {e}")))?;
    if output.status.success() {
        Ok(())
    } else {
        let stderr = String::from_utf8_lossy(&output.stderr).into_owned();
        let stdout = String::from_utf8_lossy(&output.stdout).into_owned();
        let msg = if stderr.is_empty() { stdout } else { stderr };
        Err(PlatformError::CommandFailed(format!(
            "{cmd} {} exited {}: {msg}",
            args.join(" "),
            output.status
        )))
    }
}

// ── Live regime input reader ───────────────────────────────────────────────────

/// Read the [`RegimeInputs`] from the real filesystem.
///
/// This is the only I/O entry point for detection; everything else is pure.
#[cfg(target_os = "linux")]
fn read_regime_inputs() -> RegimeInputs {
    let resolv_conf = Path::new("/etc/resolv.conf");

    // Read symlink target (if any).
    let symlink_target = std::fs::read_link(resolv_conf)
        .ok()
        .map(|p| p.to_string_lossy().into_owned());

    // Read the first 2 KiB of resolv.conf for header comments.
    let resolv_conf_head = std::fs::read_to_string(resolv_conf)
        .unwrap_or_default()
        .chars()
        .take(2048)
        .collect();

    // Check for /run/NetworkManager marker.
    let run_nm_exists = Path::new("/run/NetworkManager").exists();

    RegimeInputs {
        symlink_target,
        resolv_conf_head,
        run_nm_exists,
    }
}

// ── Current-setting reader ─────────────────────────────────────────────────────

/// Read the current effective DNS configuration for `linux:resolv.conf`.
///
/// For systemd-resolved this is always `Dhcp` (the stub 127.0.0.53 is an
/// artefact of resolved, not a user-set server).  For other regimes, we parse
/// `/etc/resolv.conf` nameserver lines.
#[cfg(target_os = "linux")]
fn read_current_setting(regime: &LinuxRegime) -> DnsSetting {
    match regime {
        LinuxRegime::SystemdResolved => {
            // The resolved drop-in is active; from resolv.conf's perspective
            // the stub (127.0.0.53) is present.  We represent this as Static
            // pointing at the stub so the drift detector can compare it to our
            // snapshot — if the resolved drop-in is still active, the stub is
            // still there.
            //
            // Actually: we check whether our drop-in is present to decide if
            // we're still "pointing at self".
            let dropin = resolved_dropin_path();
            if dropin.exists() {
                DnsSetting::Static {
                    servers: vec![
                        IpAddr::from([127, 0, 0, 1]),
                        "::1"
                            .parse()
                            .unwrap_or(IpAddr::from([0, 0, 0, 0, 0, 0, 0, 1])),
                    ],
                }
            } else {
                // Drop-in gone — drift.
                DnsSetting::Dhcp
            }
        }
        _ => {
            let content = std::fs::read_to_string("/etc/resolv.conf").unwrap_or_default();
            parse_resolv_conf_servers(&content)
        }
    }
}

// ── LinuxDns struct ────────────────────────────────────────────────────────────

/// The Linux implementation of [`PlatformDns`].
///
/// Shells out to `systemctl`, `nmcli`, and manipulates files in `/etc`.
/// Stateless; all operations execute synchronously in the calling context.
#[cfg(target_os = "linux")]
pub struct LinuxDns;

#[cfg(target_os = "linux")]
impl PlatformDns for LinuxDns {
    /// Snapshot the current DNS configuration.
    ///
    /// Detects the regime and records it in the snapshot so `restore()` can
    /// undo the correct path after a crash or reboot.
    fn snapshot(&self) -> Result<DnsSnapshot, PlatformError> {
        let inputs = read_regime_inputs();
        let regime = detect_regime(&inputs);
        debug!(?regime, "detected Linux resolver regime");

        // For the drift-detection contract: the setting at snapshot time
        // represents the pre-takeover state.  For resolved we record Dhcp
        // (the stub is resolved's artefact); for others we parse resolv.conf.
        let setting = match &regime {
            LinuxRegime::SystemdResolved => DnsSetting::Dhcp,
            _ => {
                let content = std::fs::read_to_string("/etc/resolv.conf").unwrap_or_default();
                parse_resolv_conf_servers(&content)
            }
        };

        let mut snap = DnsSnapshot::new(vec![build_linux_service(setting)]);
        snap.linux_regime = Some(regime);
        Ok(snap)
    }

    /// Point system DNS at the sinkhole (127.0.0.1 and ::1).
    ///
    /// Requires root; returns [`PlatformError::NeedsRoot`] otherwise.
    fn point_at_self(&self, _services: &[String]) -> Result<(), PlatformError> {
        check_root()?;
        let inputs = read_regime_inputs();
        let regime = detect_regime(&inputs);
        exec_takeover(&regime, Path::new("/etc/resolv.conf"))
    }

    /// Restore DNS to the state recorded in `snap`.
    ///
    /// Reads the regime from `snap.linux_regime`; falls back to re-detecting
    /// if the field is absent (forward-compat guard).
    ///
    /// **Idempotent and continues through all steps on partial failure.**
    fn restore(&self, snap: &DnsSnapshot) -> Result<(), PlatformError> {
        check_root()?;
        let regime = match &snap.linux_regime {
            Some(r) => r.clone(),
            None => {
                // Snapshot predates the linux_regime field (or is a macOS
                // snapshot loaded on Linux by mistake).  Re-detect.
                warn!("linux restore: linux_regime absent in snapshot — re-detecting");
                let inputs = read_regime_inputs();
                detect_regime(&inputs)
            }
        };
        exec_restore(&regime, Path::new("/etc/resolv.conf"))
    }

    /// Read the current DNS setting for `linux:resolv.conf`.
    ///
    /// For the drift watcher: returns the current effective setting so the
    /// sentinel can compare it against the snapshot.
    fn current_setting(&self, _service: &str) -> Result<DnsSetting, PlatformError> {
        let inputs = read_regime_inputs();
        let regime = detect_regime(&inputs);
        Ok(read_current_setting(&regime))
    }
}

